void main() {
  var nilai = 'A';

  switch (nilai) {
    case 'A':
      print('wow anda lulus dengan sangat baik');
      break;
    case 'B':
      print('anda lulus dengan baik');
      break;
    case 'C':
      print('anda lulus');
      break;
    case 'D':
      print('anda tidak lulus');
      break;
    default:
      print('pindah jurusan saja');
  }
}
